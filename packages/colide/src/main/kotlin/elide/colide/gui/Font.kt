package elide.colide.gui

import elide.colide.Vesa

/**
 * # Bitmap Font Renderer
 *
 * Simple 8x16 bitmap font for GUI text rendering.
 * Uses a compact representation where each character is defined
 * as a list of pixel rows.
 */
public object Font {
    public const val CHAR_WIDTH: Int = 8
    public const val CHAR_HEIGHT: Int = 16
    
    /**
     * Draw a string at (x, y).
     */
    public fun drawText(x: Int, y: Int, text: String, color: Int, bg: Int = Colors.TRANSPARENT) {
        var cx = x
        for (ch in text) {
            drawChar(cx, y, ch, color, bg)
            cx += CHAR_WIDTH
        }
    }
    
    /**
     * Draw a single character at (x, y).
     */
    public fun drawChar(x: Int, y: Int, ch: Char, fg: Int, bg: Int = Colors.TRANSPARENT) {
        val bitmap = getCharBitmap(ch)
        for (row in 0 until CHAR_HEIGHT) {
            val rowData = if (row < bitmap.size) bitmap[row] else 0
            for (col in 0 until CHAR_WIDTH) {
                val pixel = (rowData shr (7 - col)) and 1
                val color = if (pixel == 1) fg else bg
                if (color != Colors.TRANSPARENT) {
                    Vesa.putPixel(x + col, y + row, color)
                }
            }
        }
    }
    
    /**
     * Calculate text width in pixels.
     */
    public fun textWidth(text: String): Int = text.length * CHAR_WIDTH
    
