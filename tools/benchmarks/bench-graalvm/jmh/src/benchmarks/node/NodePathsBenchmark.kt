@file:Suppress("unused")

package benchmarks.node

import org.openjdk.jmh.annotations.BenchmarkMode
import org.openjdk.jmh.annotations.Fork
import org.openjdk.jmh.annotations.Mode
import org.openjdk.jmh.annotations.OutputTimeUnit
import org.openjdk.jmh.annotations.Scope
import org.openjdk.jmh.annotations.State
import org.openjdk.jmh.annotations.Warmup
import java.util.concurrent.TimeUnit
import kotlinx.benchmark.Benchmark
import elide.runtime.node.path.NodePaths

@BenchmarkMode(Mode.AverageTime)
@OutputTimeUnit(TimeUnit.MICROSECONDS)
@State(Scope.Benchmark)
@Fork(1)
@Warmup(iterations = 3, time = 5, timeUnit = TimeUnit.SECONDS)
class NodePathsBenchmark {
  companion object {
    private val paths = NodePaths.create()
  }

  @Benchmark fun join(): String {
    return paths.join("first", "second")
  }

  @Benchmark fun resolve(): String {
    return paths.resolve("first", "second")
  }
}
